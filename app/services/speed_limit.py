"""Per-protocol speed limit manager for IronPanel.

Limits are saved in AppSetting and applied with Linux tc/HTB on the default
network interface. The limiter is intentionally protocol/port based so it is
simple for admins to understand: set Mbps for a protocol, apply, and traffic
sent by the server from that protocol port is capped.
"""
from __future__ import annotations
import re, shlex
from pathlib import Path
from ..core.extensions import db
from ..core.models import AppSetting
from .provisioning import get_setting, set_setting, get_port, active_protocols, run_cmd

